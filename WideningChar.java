class WideningChar 
{
	public static void main(String[] args) 
	{
		char a = 'v';
		
		byte b = a;
		System.out.println(b);
		
		short h = a;
		System.out.println(h);
		
		int b = a;
		System.out.println(b);
		
		long c = a;
		System.out.println(c);
		
		float d = a;
		System.out.println(d);
		
		double e = a;
		System.out.println(e);
	}
}
